def soma(num1, num2):
    """ Função de Soma """
    n_1 = num1 + num2 
    return n_1


def subt(num1, num2):
    """ Função de Subtração """
    n_1 = num1 - num2
    return n_1


def mult(num1, num2):
    """ Função de Multiplicação """
    n_1 = num1 * num2
    return n_1


def divi(num1, num2):
    """ Função de Divisao """
    n_1 = num1 / num2
    return n_1
