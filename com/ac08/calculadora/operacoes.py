def soma (num1, num2):
	""" Função de Soma """
	n1 = num1 + num2 
	return n1


def subt (num1, num2):
	""" Função de Subtração """
	n1 = num1 - num2
	return n1


def mult(num1, num2):
	""" Função de Multiplicação """
	n1 = num1 * num2
	return n1


def divi(num1, num2):
	""" Função de Divisao """
	n1 = num1 / num2
	return n1
